/* USER CODE BEGIN Header_StartTask01 */
/**
* @brief Function implementing the myTask01 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask01 */
void StartTask01(void *argument)
{
  /* USER CODE BEGIN StartTask01 */
  osStatus_t result = 0;
  /* Infinite loop */
  for(;;)
  {
    result = osEventFlagsWait(myEvent01Handle, 0xffffff, osFlagsWaitAny | osFlagsNoClear, 0xff);

    if(result > 0)
    {
      printf("rec = %d\r\n", osEventFlagsGet(myEvent01Handle));
      osEventFlagsClear(myEvent01Handle, 0xffffff);
    }
    
    osDelay(1); // 500ms
  }
  /* USER CODE END StartTask01 */
}

